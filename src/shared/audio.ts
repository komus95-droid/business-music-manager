/** Длительность плавного затухания в конце рабочего дня. */
export type EndOfDayFadeSec = 10 | 20 | 30;

/** Допустимый диапазон кроссфейда / нахлёста, секунды. */
export const FADE_OVERLAP_MIN = 0;
export const FADE_OVERLAP_MAX = 20;

export interface AudioSettings {
  /** мастер-громкость, 0..100 */
  volume: number;
  /** авто-дакинг: до какого % приглушать музыку под объявление, 0..100 */
  ducking: number;
  /** кроссфейд / нахлёст между блоками и треками, секунды (0..20) */
  fadeOverlap: number;
  /** сглаживание переходов, 0..100 */
  smoothing: number;
  /** 10 полос эквалайзера, каждая 0..100 */
  eq: EqBands;
  /** активный пресет; null — если ползунки правились вручную */
  eqPreset: EqPresetName | null;
  /** плавное затухание в конце рабочего дня */
  endOfDayFadeSec: EndOfDayFadeSec;
}
